/** Get weibo posts matching today's month+day from any year */
export function getTodayWeibo(month: number, day: number): WeiboPost[] {
  const mm = String(month).padStart(2, '0')
  const dd = String(day).padStart(2, '0')
  return weiboPosts.filter(w => {
    const parts = w.date.split('-')
    return parts[1] === mm && parts[2] === dd
  })
}

/** Get random weibo from same month across years */
export function getMonthWeibo(month: number): WeiboPost[] {
  const mm = String(month).padStart(2, '0')
  const monthPosts = weiboPosts.filter(w => w.date.split('-')[1] === mm)
  // shuffle and pick up to 5
  const shuffled = [...monthPosts].sort(() => Math.random() - 0.5)
  return shuffled.slice(0, 5)
}
