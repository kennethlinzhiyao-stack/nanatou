export function getFortuneLevel(type: Fortune['type']): '吉' | '凶' {
  return type === '下签' ? '凶' : '吉'
}

export function splitPoemLines(poem: string): string[] {
  return poem.split(/[，。]/).filter(l => l.trim().length > 0)
}

export function getRandomFortune(excludeIds: number[] = []): Fortune {
  const available = fortunes.filter(f => !excludeIds.includes(f.id))
  return available[Math.floor(Math.random() * available.length)]
}
